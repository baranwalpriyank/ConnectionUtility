package com.junit.pojo;


public class EmployeeClass {

	private String name;
	
	public void setName(String name){
		this.name=name;
	}
	public String getName(){
		return this.name;
	}
	
	private long salary;
	
	public void setSalary(long sal){
		this.salary=sal;
	}
	public long getSalary(){
		return this.salary;
	}
	
	private int age;
	
	public void setAge(int age){
		this.age = age;
	}
	public int getAge(){
		return this.age;
	}
	
}
